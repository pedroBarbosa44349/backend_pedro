function subtracao(n1,n2){
    const resultado = n1 - n2
    console.log("Resultado subtracao: " + resultado)
}

function adição(n1,n2){
    const resultado = n1 + n2
    console.log("Resultado adição: " + resultado)
}

function divisão(n1,n2){
    const resultado = n1 / n2
    console.log("Resultado divisão: " + resultado)
}

function multiplicacao(n1,n2){
    const resultado = n1 * n2
    console.log("Resultado multiplicacao: " + resultado)
}

